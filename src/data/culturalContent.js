// Helper functions to retrieve cultural content

export const getCulturalByLanguage = (language) => {
  return culturalContent[language] || {};
};

export const getCulturalByCategory = (language, category) => {
  const languageCultural = culturalContent[language];
  if (!languageCultural) return [];
  return languageCultural[category] || [];
};

export const getAllCulturalArticles = (language) => {
  const languageCultural = culturalContent[language];
  if (!languageCultural) return [];
  return Object.values(languageCultural).flat();
};

export const getCulturalArticle = (language, articleId) => {
  const allArticles = getAllCulturalArticles(language);
  return allArticles.find(article => article.id === articleId);
};

export const getCulturalArticleCount = (language) => {
  return getAllCulturalArticles(language).length;
};

export const getAvailableCategories = (language) => {
  const languageCultural = culturalContent[language];
  if (!languageCultural) return [];
  return Object.keys(languageCultural);
};
