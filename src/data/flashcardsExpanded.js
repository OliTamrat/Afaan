// Helper function to get all flashcards for a language
export const getAllFlashcards = (language) => {
  const data = flashcardsExpanded[language];
  if (!data) return [];

  // Flatten all categories into single array
  return Object.values(data).flat();
};

// Helper function to get flashcards by level
export const getFlashcardsByLevel = (language, level) => {
  const allCards = getAllFlashcards(language);
  return allCards.filter(card => card.level === level);
};

// Helper function to get flashcards by difficulty
export const getFlashcardsByDifficulty = (language, difficulty) => {
  const allCards = getAllFlashcards(language);
  return allCards.filter(card => card.difficulty === difficulty);
};

// Get count of cards per language
export const getFlashcardCount = (language) => {
  return getAllFlashcards(language).length;
};

